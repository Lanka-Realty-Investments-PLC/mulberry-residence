<script>
    $(document).ready(function () {
        $("body").on("submit", "#letterform", function (e) {
            e.preventDefault();

            var msg = $('#lettermsg');
            var emailinput = $('#letteremail');

            if (emailinput.val() != '') {
                msg.text('');
                var btn = $("#letterbtn");
                btn.attr("disabled", true);

                $.ajax({
                    url: "newsletter_ajax.php",
                    type: "POST",
                    processData: false,
                    contentType: false,
                    data: new FormData(this),
                    dataType: 'json',
                    success: function (data) {
                        emailinput.val('');

                        if (data.status == true) {
                            btn.attr("disabled", false);
                            msg.text('You have successfully subscribed to our newsletter');
                        } else {
                            msg.text('You have already subscribed to our newsletter');
                            btn.attr("disabled", false);
                        }
                    },
                    error: function () {
                        alert('Operation failed. Please try again.');
                        btn.attr("disabled", false);
                    }
                });
            }

        });
    });

